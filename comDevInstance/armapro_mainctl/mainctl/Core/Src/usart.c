#include "util.h"

#define MAX_TRAN_LEN		128
#define LOH_HANDLE			huart1

uint8_t transmitBuf[MAX_TRAN_LEN];

void printf_DMA(char *fmt,...)
{
	uint8_t n=0;
	memset(transmitBuf,0,sizeof(uint8_t)*MAX_TRAN_LEN);
	va_list arg;
	va_start(arg,fmt);							//将...中的输入与fmt初始化到arg
	sprintf((char*)transmitBuf, fmt, arg);		//将输出存到发送缓存中
	va_end(arg);
	n = MAX_TRAN_LEN;							//发送最大字符，硬件处理
	HAL_UART_Transmit_DMA(&LOH_HANDLE,(uint8_t*)&transmitBuf, n);  //通过DMA发送字符串
}

int fputc(int ch, FILE *f) {
 HAL_UART_Transmit(&LOH_HANDLE, (uint8_t *)&ch, 1, 0xFFFF);
 return ch;
}

